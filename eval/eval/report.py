"""Report generator for Aegis Health evaluation results.

Reads a results JSON file produced by the runner and generates a human-readable
markdown report with per-category tables and aggregate scores.

Usage:
    python -m eval.report eval/reports/eval_results_run_20250101T000000Z.json
"""

from __future__ import annotations

import argparse
import json
from pathlib import Path
from typing import Any

REPORTS_DIR = Path(__file__).resolve().parent.parent / "reports"
METRIC_NAMES = [
    "json_validity",
    "deferral_accuracy",
    "citation_presence",
    "safety_boundary",
    "severity_accuracy",
]


def load_results(results_path: Path) -> dict[str, Any]:
    with open(results_path) as f:
        return json.load(f)


def _fmt(val: float) -> str:
    """Format a score as a percentage string."""
    return f"{val * 100:.1f}%"


def _status_icon(val: float) -> str:
    if val >= 0.9:
        return "PASS"
    elif val >= 0.5:
        return "WARN"
    return "FAIL"


def build_report(data: dict[str, Any]) -> str:
    """Build a markdown report string from results data."""
    lines: list[str] = []
    tag = data.get("tag", "unknown")
    timestamp = data.get("timestamp", "unknown")
    num_cases = data.get("num_cases", 0)
    summary = data.get("summary", {})
    results = data.get("results", [])

    lines.append(f"# Aegis Health Eval Report")
    lines.append("")
    lines.append(f"- **Tag:** {tag}")
    lines.append(f"- **Timestamp:** {timestamp}")
    lines.append(f"- **Total cases:** {num_cases}")
    lines.append("")

    overall = summary.get("overall", {})
    if overall:
        lines.append("## Overall Scores")
        lines.append("")
        lines.append("| Metric | Score | Status |")
        lines.append("|--------|-------|--------|")
        for m in METRIC_NAMES:
            val = overall.get(m, 0.0)
            lines.append(f"| {m} | {_fmt(val)} | {_status_icon(val)} |")

        grand_avg = sum(overall.get(m, 0.0) for m in METRIC_NAMES) / len(METRIC_NAMES)
        lines.append(f"| **AVERAGE** | **{_fmt(grand_avg)}** | **{_status_icon(grand_avg)}** |")
        lines.append("")

    per_category = summary.get("per_category", {})
    if per_category:
        lines.append("## Per-Category Scores")
        lines.append("")

        header = "| Category | " + " | ".join(METRIC_NAMES) + " | Avg |"
        separator = "|----------|" + "|".join(["------"] * len(METRIC_NAMES)) + "|-----|"
        lines.append(header)
        lines.append(separator)

        for cat in sorted(per_category.keys()):
            scores = per_category[cat]
            vals = [scores.get(m, 0.0) for m in METRIC_NAMES]
            avg = sum(vals) / len(vals) if vals else 0.0
            row = f"| {cat} | " + " | ".join(_fmt(v) for v in vals) + f" | {_fmt(avg)} |"
            lines.append(row)

        lines.append("")

    if results:
        lines.append("## Per-Case Results")
        lines.append("")

        cats: dict[str, list[dict]] = {}
        for r in results:
            cats.setdefault(r["category"], []).append(r)

        for cat in sorted(cats.keys()):
            lines.append(f"### {cat}")
            lines.append("")
            lines.append("| Case ID | " + " | ".join(METRIC_NAMES) + " | Avg |")
            lines.append("|---------|" + "|".join(["------"] * len(METRIC_NAMES)) + "|-----|")

            for r in cats[cat]:
                scores = r.get("scores", {})
                vals = [scores.get(m, 0.0) for m in METRIC_NAMES]
                avg = sum(vals) / len(vals) if vals else 0.0
                row = (
                    f"| {r['case_id']} | "
                    + " | ".join(_fmt(v) for v in vals)
                    + f" | {_fmt(avg)} |"
                )
                lines.append(row)

            lines.append("")

    lines.append("---")
    lines.append("*Generated by Aegis Health eval pipeline*")
    lines.append("")

    return "\n".join(lines)


def generate_report(results_path: Path) -> Path:
    """Load results and write a markdown report alongside them."""
    data = load_results(results_path)
    report_md = build_report(data)

    report_path = REPORTS_DIR / "eval_report.md"
    REPORTS_DIR.mkdir(parents=True, exist_ok=True)
    with open(report_path, "w") as f:
        f.write(report_md)

    print(f"Report saved to {report_path}")
    return report_path


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Generate a markdown eval report from results JSON",
    )
    parser.add_argument(
        "results_file",
        type=str,
        help="Path to the eval results JSON file",
    )
    args = parser.parse_args()

    results_path = Path(args.results_file)
    if not results_path.exists():
        print(f"ERROR: Results file not found: {results_path}")
        raise SystemExit(1)

    generate_report(results_path)


if __name__ == "__main__":
    main()
